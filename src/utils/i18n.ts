const dict: Record<string, Record<string, string>> = { ro, en }

export function t(key: string, lang: 'ro' | 'en' = 'ro', vars?: Record<string, string>): string {
  let text = dict[lang]?.[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{{${k}}}`, v)
    }
  }
  return text
}
